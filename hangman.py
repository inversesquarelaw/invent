def getRandomWord(wordList):
    #picks a word from list that player will be guessing
    wordIndex = random.randint(0, len(wordList) - 1)
    return wordList[wordIndex]

def displayBoard(HANGMANPICS, missedLetters, correctLetters, secretWord):
    print(HANGMANPICS[len(missedLetters)])
    print()

    print('Missed letters:', end=' ')
    for letter in missedLetters:
        print(letter, end=' ')
    print()

    blanks = '_' * len(secretWord)

    #for 0 to length of secretWord-1
    for i in range(len(secretWord)):       
        #for each letter in the secret word, if letter is correctly
        #guessed, then replace '_' with that letter
        #if 1st letter in correctLetters guess, 2nd letter, 3rd letter, etc.
        if secretWord[i] in correctLetters:
            #replace blanks with correct letters
            blanks = blanks[:i] + secretWord[i] + blanks[i+1:]

    #print the secret word with blanks for unguessed letters
    for letter in blanks: 
        print(letter, end=' ')
    print()

def getGuess(alreadyGuessed):
    #get and sanatize user input
    while True:
        print('Guess a letter.')
        guess = input()
        guess = guess.lower()
        if len(guess) != 1:
            print('Please enter a single letter.')
        elif guess in alreadyGuessed:
            print('You have already guessed that letter. Choose again.')
        elif guess not in 'abcdefghijklmnopqrstuvwxyz':
            print('Please enter a LETTER.')
        else:
            return guess

def playAgain():
    print('Do you want to play again? (yes or no)')
    return input().lower().startswith('y')

#the program starts here
print('H A N G M A N')
missedLetters = ''
